INICIO:
	CLR A
	CLR B
	CLR C
	SETB P0.0
	SETB P0.1
	SETB P0.2
	SETB P0.3
	SETB P0.4
	SETB P0.5
	CLR P0.6
	CLR P1.0
	CLR P1.1
	CLR P3.0
	CLR P3.1
	SETB P3.2
	SETB P3.3
	CLR P3.4
	CLR P3.5
	CLR P2.5
	JMP QUIETO

QUIETO:
	CALL GENEV
	MOV A,B
	SUBB A,#01H
	JZ EVENTO1
	SUBB A,#01H
	JZ EVENTO2
	SUBB A,#09H
	JZ EVENTO11
	MOV A,B
	SUBB A,#00H
	JZ QUIETO
	JMP ERROR

EVENTO1:
	JMP ACELERAR_SUBIENDO

EVENTO2:
	JMP ACELERAR_BAJANDO

ACELERAR_SUBIENDO:
	CALL GENEV
	MOV A,B
	SUBB A,#03H
	JZ EVENTO3
	SUBB A,#02H
	JZ EVENTO5
	MOV A,B
	SUBB A,#00H
	JZ ACELERAR_SUBIENDO
	JMP ERROR

EVENTO3:
	MOV A,C
	ADD A,#05H
	MOV C,A
	MOV PWM0,A
	JMP ACELERAR_SUBIENDO

EVENTO5:
	JMP SUBIR

SUBIR:
	CALL GENEV
	MOV A,B
	SUBB A,#07H
	JZ EVENTO7
	MOV A,B
	SUB A,#00H
	JZ SUBIR
	JMP ERROR

EVENTO7:
	JMP FRENAR

FRENAR:	
	CALL GENEV
	MOV A,B
	SUBB A,#09H
	JZ EVENTO9
	SUBB A,#01H
	JZ EVENTO10
	MOV A,B
	SUBB A,#00H
	JZ FRENAR
	JMP ERROR

EVENTO9:
	MOV A,C
	SUBB A,#05H
	MOV PWM0,A
	MOV C,A
	JMP FRENAR

EVENTO10:
	MOV A,C
	SUBB A,#05H
	MOV C,A
	MOV PWM0,A
	JMP ABRIR

ABRIR:
	;POR HACER

CERRAR:
	;POR HACER

ACELERAR_BAJANDO:
	CALL GENEV
	MOV A,B
	SUBB A,#04H
	JZ EVENTO4
	SUBB A,#02H
	JZ EVENTO6
	MOV A,B
	SUBB A,#00H
	JZ ACELERAR_BAJANDO
	JMP ERROR

EVENTO4:
	MOV A,C
	ADD A,#05H
	MOV C,A
	MOV PWM0,A
	JMP ACELERAR_BAJANDO

EVENTO6:
	JMP BAJAR

BAJAR:	
	CALL GENEV
	MOV A,B
	SUBB A,#08H
	JZ EVENTO8
	MOV A,B
	SUBB A,#00H
	JZ BAJAR
	JMP ERROR

EVENTO8:
	JMP FRENAR

EVENTO11:;ESTA EN EL PISO DESDE EL QUE LLAMAS
	JMP ABRIR

